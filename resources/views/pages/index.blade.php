<h>About</h>
<p>Greenville Parking is a system that incorporates an app based user interface as well as a mobile web-app based company and attendant interface to facilitate finding and reserving parking downtown.</p>

<p>The system will save time and help parking attendants avoid turning away customers, while also helping users find available parking nearby where they need to be, and know how much it will cost.</p>

<p>People looking for parking downtown are able to reserve and pre-pay for a parking spot, and the attendant will get a notification to be able to set aside parking spots for those pre-purchased.</p>
